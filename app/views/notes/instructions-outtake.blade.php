<p>This process will transition a current shelter resident out of our shelter. Please provide detail in the note regarding the resident’s transition (ex. where they are moving to, note-worthy occurrences upon departure, etc.)</p>

<p>
    If you know the individual’s new address, please input this information into the appropriate fields.
</p>

<p>
When this process is completed:
    <ul>
    <li>The individual’s status will be modified to shelter former resident</li>
    <li>Their bed will be unoccupied and marked as requiring cleaning</li>
    <li>Their locker (if applicable) will be unoccupied and marked as requiring cleaning</li>
    <li>The note (as filled out) will be placed in their profile</li>
    <li>A note will be placed on the dashboard informing of their transition</li>
    <li>The status update will be marked on the dashboard</li>
</ul>
</p>