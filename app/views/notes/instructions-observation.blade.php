<p>
This process will document an occurrence with one individual. As a rule of thumb, a note-worthy observation would be any occurrence that could potentially provide insight to staff in how best to serve an individual – could be something that may require follow-up, or alerting to potential concerns. As a rule of thumb: if in doubt, note it (more communication is generally better communication). Please provide detail in the note regarding the interaction.
</p>
<p>
This note type can be used for:
    <ul>
    <li>Staff observations (odd behaviours) of an individual’s well-being</li>
    <li>Relevant life circumstances of a specific individual (family member passing away, etc.)</li>
    <li>Required Follow-up</li>
    <li>Minor behaviours (growing tension in a dorm, etc.)</li>
</ul>
</p>
<p>


Please note – this does not change statuses, residency, or assign consequences to the incident – it only documents the occurrence.
</p>
<p>


When this process is completed:
    <ul>
<li>The note (as filled out) will be placed in their profile</li>
<li>A note will be placed on the dashboard informing of this interaction</li>
</ul>
</p>