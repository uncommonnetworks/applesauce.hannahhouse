<p>This process will create a note on the dashboard, accessible to both staff and volunteers. It is not tethered to an individual’s profile.</p>
<p>
This note type can be used for:
    <ul>
    <li>General reminders</li>
    <li>Information pertinent to daily operations (volunteer absences, facility usage, Shelter program information, resident appoinements/reminders etc.)</li>
    <li>Shift hand over information</li>
</ul>
</p>
<p>
Please note – if a note requires permanence on the dashboard for an extended amount of time, you can flag a note as “important” after posting it, by selecting Make Important.
</p>
