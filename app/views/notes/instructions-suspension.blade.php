<p>This process will:
<ol>
    <li>Transition a current resident out of our shelter due to a behavioural offense.</li>
    <li>Change the status of a former resident due to a behavioural offense.</li>
</ol>

<p>
    Please provide detail in the note regarding the resident’s transition (ex. reason for suspension, duration / type of suspension, individual’s response to suspension, note-worthy occurrences upon departure, etc.)
</p>

<p>
Please input the suspension type (overnight, programs only, shelter) & duration (3, 7, 30, 90, indefinite).
</p>

<p>
When this process is completed:
    <ul>

	<li>The individual’s status will be modified to shelter former resident > suspended</li>
	<li>The suspension & detail will be accessible from the “Suspensions” page</li>
    <li>Their bed will be unoccupied and marked as requiring cleaning</li>
    <li>Their locker (if applicable) will be unoccupied and marked as requiring cleaning</li>
    <li>The note (as filled out) will be placed in their profile</li>
    <li>A note will be placed on the dashboard informing of their transition</li>
    <li>The status update will be marked on the dashboard</li>
    </ul>
</p>
<p>
Reminder of behaviours that warrant a suspension:
    <ul>


    <li>Involvement in a verbal or physically abusive altercation with another resident, staff or volunteer</li>
    <li>Possession of any alcohol, narcotics, drug paraphernalia, pornography, weapons, unregistered medication, or other contraband; this includes any contraband found on the shelter property whereby there is sufficient reason to believe that the resident in question is or was in possession of said material (for example, near their bed or locker, where they had recently been, etc)</li>
    <li>Suspicion of being under the influence of alcohol or narcotics</li>
    <li>Theft or vandalism</li>
    <li>Sexual activity or soliciting</li>
    <li>Missing curfew</li>
    <li>Smoking inside the shelter</li>
    <li>Using/abusing shelter/staff property or property of others in any way</li>
    <li>Disobeying staff requests/restrictions</li>
</ul>
</p>