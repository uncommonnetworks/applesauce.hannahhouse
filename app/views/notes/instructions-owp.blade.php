<p>
    This process will change the status of a current shelter resident to Out With Permission (OWP is an exception to stay out past curfew or for a night).
    Please provide detail in the note regarding the resident’s status (ex. rationale for exception, expected return,
    interesting occurrences around status change, contact information while out, etc.)
</p>
<p>
When this process is completed:
<ul>

    <li>The individual’s status will be modified to OWP</li>
    <li>Their bed will be occupied but will show as a different colour</li>
    <li>Their locker (if applicable) will remain occupied</li>
    <li>The note (as filled out) will be placed in their profile</li>
    <li>A note will be placed on the dashboard informing of their transition</li>
    <li>The status update will be marked on the dashboard</li>
</ul>
</p>
<p>
Reminder of valid exceptions:
<ul>


    <li>Personal medical emergency (at hospital): Resident must return with hospital bracelet and have hospital staff call to verify hospital visit</li>
    <li>Verified employment: Resident must arrange with staff ahead of time, provide supervisor’s/company’s contact name and number, specify work days/hours, and return directly at end of shift</li>
    <li>Out of town court date or appointment: Resident must arrange with staff ahead of time, provide documentation to verify appointment, provide name and contact number of place individual is staying for the night</li>
    <li>Immediate family emergency (e.g. death, sudden sickness, sudden change in health): Resident must arrange with staff before leaving, provide name and contact number of familial contact, date of return to the shelter</li>
    <li>Other rare circumstances based on the discretion of the RSM</li>
</ul>
</p>
<p>
REMINDER – When a resident is OWP for medical emergency at the hospital, the hospital must be called EVERY EVENING to verify that they are still in the hospital.
</p>